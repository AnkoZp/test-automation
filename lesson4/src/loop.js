// Cycle from 0 to 9
//for
for (let i = 0; i <= 9; i++) {
    console.log('Iteration (0 > 9):', i);
}

//while
let a = 0;
while (a <= 9) {
    console.log('While loop iteration:', a);
    a++;
}

//do while
let b = 0;
do {
    console.log('Do...while loop iteration:', b);
    b++;
} while (b <= 9);

// Cycle from 100 to 0 in increments of 10
//for
for (let j = 100; j >= 0; j -= 10) {
    console.log('Iteration (100 > 0):', j);
}

//while
let c = 100;
do {
    console.log('While loop iteration (100 > 0):', c);
    c -= 10;
} while (c >= 0);

//do while
let d = 100;
do {
    console.log('Do...while loop iteration (100 > 0):', d);
    d -= 10;
} while (d >= 0);

