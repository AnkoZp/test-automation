// Numerical variables
let a = 15;
let b = 8;

// String variables
const str1 = "Hello";
const str2 = "World";

// Arithmetic operations with numbers
console.log("a + b =", a + b);  // 23
console.log("a - b =", a - b);  // 7
console.log("a * b =", a * b);  // 120
console.log("a / b =", a / b);  // 1.875
console.log("a ** b =", a ** b);  // 2562890625
console.log("a += a =", a += a );  // 30
console.log("b -= b =", b -= b);  // 0

// Arithmetic operations with strings
console.log("str1 + str2 =", str1 + " " + str2);

// Number and string concatenation
console.log("a + str1 =", a + str1); // 15Hello
